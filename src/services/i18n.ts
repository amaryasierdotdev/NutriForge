  static setLanguage(languageCode: string): void {
    if (this.languages[languageCode]) {
      this.currentLanguage = languageCode
      localStorage.setItem('language', languageCode)
    }
  }

  static getCurrentLanguage(): string {
    return this.currentLanguage
  }

  static getAvailableLanguages(): Language[] {
    return Object.values(this.languages)
  }

  static t(key: string, params?: Record<string, string>): string {
    const keys = key.split('.')
    let value: any = this.languages[this.currentLanguage]?.translations

    for (const k of keys) {
      value = value?.[k]
    }

    if (typeof value !== 'string') {
      return key // Return key if translation not found
    }

    // Replace parameters
    if (params) {
      Object.entries(params).forEach(([param, replacement]) => {
        value = value.replace(new RegExp(`{${param}}`, 'g'), replacement)
      })
    }

    return value
  }

  static init(): void {
    const savedLanguage = localStorage.getItem('language')
    if (savedLanguage && this.languages[savedLanguage]) {
      this.currentLanguage = savedLanguage
    }
  }
